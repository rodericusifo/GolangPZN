package main

import "fmt"

func main() {
	var name string

	name = "Rodericus Ifo"
	fmt.Println(name)

	name = "Rodericus Krista"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Rodericus"
		lastName  = "Krista"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
